//*****************************************************************************
//
//! SPI Master mode main loop
//!
//! This function configures SPI modelue as master and enables the channel for
//! communication
//!
//! \return None.
//
//*****************************************************************************
void MasterMain()
{
    //
    // Reset SPI
    //
    MAP_SPIReset(GSPI_BASE);

    //
    // Configure SPI interface
    //
    MAP_SPIConfigSetExpClk(GSPI_BASE,MAP_PRCMPeripheralClockGet(PRCM_GSPI),
                     SPI_IF_BIT_RATE,SPI_MODE_MASTER,SPI_SUB_MODE_0,
                     (SPI_SW_CTRL_CS |
                     SPI_4PIN_MODE |
                     SPI_TURBO_OFF |
                     SPI_CS_ACTIVEHIGH |
                     SPI_WL_8));

    //
    // Enable SPI for communication
    //
    MAP_SPIEnable(GSPI_BASE);

    // initializing OLED
    Adafruit_Init();

    fillScreen(BLACK);

    while (1)
    {
        // font
        fillScreen(BLACK);
        setTextColor(WHITE, BLACK);
        setTextSize(1);
        int i=0;
        int j=0;
        for(i=0; i<sizeof(font);i++){
            Outstr(font[i]);
            if(i%32==0){ // reached end of row
                setCursor(0, j+=8); // hardcoded value of 8 to give the impression of a row
            }
        }

        // string
        fillScreen(BLACK);
        setCursor(0, 0); // cursor is outside the screen from previous test
        setTextColor(GREEN, BLACK);
        setTextSize(2);
        Outstr("Hello ");
        setTextColor(MAGENTA, BLUE);
        Outstr("world!");
        delay(100);

        // 8 horizontal bands

        lcdTestPattern();
        delay(100);

        // 8 vertical bands

        lcdTestPattern2();
        delay(100);

        // testlines()

        testlines(RED);
        delay(100);

        // testfastlines()

        testfastlines(BLUE, YELLOW);
        delay(100);

        // testdrawrects()

        testdrawrects(MAGENTA);
        delay(100);

        // testfillrects()

        testfillrects(CYAN, YELLOW);
        delay(1);

        // testfillcircles()

        testfillcircles(10, WHITE);
        delay(1);

        // testroundrects()

        testroundrects();
        delay(1);

        // testtriangles()

        testtriangles();
        delay(100);

    }
    
    //
    // Enable and disable Chip select
    //
    MAP_SPICSEnable(GSPI_BASE);
    MAP_SPICSDisable(GSPI_BASE);
    
}

//*****************************************************************************
//
//! Board Initialization & Configuration
//!
//! \param  None
//!
//! \return None
//
//*****************************************************************************
static void
BoardInit(void)
{
/* In case of TI-RTOS vector table is initialize by OS itself */
#ifndef USE_TIRTOS
  //
  // Set vector table base
  //
#if defined(ccs)
    MAP_IntVTableBaseSet((unsigned long)&g_pfnVectors[0]);
#endif
#if defined(ewarm)
    MAP_IntVTableBaseSet((unsigned long)&__vector_table);
#endif
#endif
    //
    // Enable Processor
    //
    MAP_IntMasterEnable();
    MAP_IntEnable(FAULT_SYSTICK);

    PRCMCC3200MCUInit();
}

//*****************************************************************************
//
//! Main function for spi demo application
//!
//! \param none
//!
//! \return None.
//
//*****************************************************************************
void main()
{
    //
    // Initialize Board configurations
    //
    BoardInit();

    //
    // Muxing UART and SPI lines.
    //
    PinMuxConfig();

    //
    // Enable the SPI module clock
    //
    MAP_PRCMPeripheralClkEnable(PRCM_GSPI,PRCM_RUN_MODE_CLK);

    //
    // Initialising the Terminal.
    //
    InitTerm();

    //
    // Clearing the Terminal.
    //
    ClearTerm();

    //
    // Display the Banner
    //
    Message("\n\n\n\r");
    Message("\t\t   ********************************************\n\r");
    Message("\t\t        CC3200 SPI OLED Application  \n\r");
    Message("\t\t   ********************************************\n\r");
    Message("\n\n\n\r");

    //
    // Reset the peripheral
    //
    MAP_PRCMPeripheralReset(PRCM_GSPI);

#if MASTER_MODE

    MasterMain();

#endif


}
